


//funcção contendo a impresão de um nome
fun novaFuncao() {
    println("oi, tudo bem")
 
}

//função com paramentro e contendo a impressao 

private fun novaFuncao2(nome: String) {
    println("oi, $nome")
 
}

//funcão imprimindo as duas funções feita a cima

fun main() {
    novaFuncao()
    novaFuncao2("Daniel")
 
}

//impressão da função

//oi, tudo bem
//oi, Daniel