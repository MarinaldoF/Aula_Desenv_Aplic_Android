//função contendo uma lista
//É feito uma estrutura de repetição da lista pra imprimir 
//Feito um estrutura de repetição com for para imprimir 5 vezes 


fun main() {
    val nomes = listOf("Daniel", "Domingos", "Akira")
   	for (nome in nomes) {
        println(nome)
    }
    
    	for (i in 1..5) {
        println(i)
    }
        for (i in 1 until 5) {
        println(i)
    }
    
}